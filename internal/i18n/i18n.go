// Package i18n provides a tiny, dependency-free translation layer with two
// bundled locales (Persian and English). Lookups never fail: an unknown key
// falls back to English and then to the raw key, so the UI is always rendered.
package i18n

import (
	"fmt"
	"strings"
)

// Lang is a supported UI language code.
type Lang string

const (
	FA Lang = "fa"
	EN Lang = "en"
)

// Normalize coerces an arbitrary string into a supported language.
func Normalize(s string) Lang {
	switch strings.ToLower(strings.TrimSpace(s)) {
	case "en", "english":
		return EN
	default:
		return FA
	}
}

// T returns the translation for key in lang, formatting with args (fmt-style).
func T(lang Lang, key string, args ...any) string {
	tbl, ok := tables[lang]
	if !ok {
		tbl = tables[EN]
	}
	s, ok := tbl[key]
	if !ok {
		if s, ok = tables[EN][key]; !ok {
			s = key
		}
	}
	if len(args) > 0 {
		return fmt.Sprintf(s, args...)
	}
	return s
}

